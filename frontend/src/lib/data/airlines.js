/**
 * Look up an airline name from the first two characters of a flight number.
 * Returns the airline name string, or empty string if not found.
 * @param {string} flightNumber
 * @returns {string}
 */
export function lookupAirline(flightNumber) {
  if (!flightNumber || flightNumber.length < 2) return '';
  const code = flightNumber.slice(0, 2).toUpperCase();
  return AIRLINE_CODES[code] ?? '';
}
